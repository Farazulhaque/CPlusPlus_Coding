#include <iostream>
using namespace std;

int main()
{
    int x = 2, y = 5, z = 6;
    // Q1
    cout << "x = " << x << ", y = " << y << ", z = " << z << endl;
    cout << "x + y = " << x + y << endl;
    cout << "Sum of " << x << " and " << z << " is " << x + z << endl;
    cout << "z / x = " << z / x << endl;
    cout << "2 times " << x << " = " << 2 * x << endl;
    return 0;
}